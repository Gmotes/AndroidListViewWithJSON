package com.example.androidlistviewwithjson.model;

public class Match {
	
	private String homeTeam;
	private String awayTeam;
	private String homeGoalTeam;
	private String awayGoalTeam;
	private String description;
	
	
	public String getHomeTeam() {
		return homeTeam;
	}
	public void setHomeTeam(String homeTeam) {
		this.homeTeam = homeTeam;
	}
	public String getAwayTeam() {
		return awayTeam;
	}
	public void setAwayTeam(String awayTeam) {
		this.awayTeam = awayTeam;
	}
	public String getHomeGoalTeam() {
		return homeGoalTeam;
	}
	public void setHomeGoalTeam(String homeGoalTeam) {
		this.homeGoalTeam = homeGoalTeam;
	}
	public String getAwayGoalTeam() {
		return awayGoalTeam;
	}
	public void setAwayGoalTeam(String awayGoalTeam) {
		this.awayGoalTeam = awayGoalTeam;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}

}
